#Range()
      # Range function are used to return a sequence of numbers. starting from 0 by default and increase by 1 (by default), and stop before a specified number.

seq = range(5)

for i in seq:
      print(i)


for i in range(100):
      print(i)

for i in range(2,100): # start from 2 to 99
      print(i)

for i in range(2, 100, 2): # to print even num between 2 to 100
      print(i)

for i in range(1, 100, 2): # tp print odd num between 1 to 100
      print(i)
